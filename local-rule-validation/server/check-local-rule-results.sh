#!/usr/bin/env bash
set -euo pipefail

RUN_ID=""
MANIFEST="./local_rule_tests.json"
ENDPOINT_RESULTS=""
ALERTS="/var/ossec/logs/alerts/alerts.json"
OUT_DIR="./validation-results"

usage() {
  cat <<'USAGE'
Usage:
  check-local-rule-results.sh --run-id RUN_ID [--manifest local_rule_tests.json] [--endpoint-results endpoint-results.jsonl] [--alerts /var/ossec/logs/alerts/alerts.json] [--out-dir DIR]

Checks Wazuh alerts.json for local rule hits generated by endpoint validation scripts.
USAGE
}

while [[ $# -gt 0 ]]; do
  case "$1" in
    --run-id) RUN_ID="${2:-}"; shift 2 ;;
    --manifest) MANIFEST="${2:-}"; shift 2 ;;
    --endpoint-results) ENDPOINT_RESULTS="${2:-}"; shift 2 ;;
    --alerts) ALERTS="${2:-}"; shift 2 ;;
    --out-dir) OUT_DIR="${2:-}"; shift 2 ;;
    -h|--help) usage; exit 0 ;;
    *) echo "Unknown argument: $1" >&2; usage; exit 2 ;;
  esac
done

if [[ -z "$RUN_ID" ]]; then
  echo "Missing --run-id" >&2
  usage
  exit 2
fi
if [[ ! -f "$MANIFEST" ]]; then
  echo "Manifest not found: $MANIFEST" >&2
  exit 2
fi
if [[ ! -f "$ALERTS" ]]; then
  echo "Alerts log not found: $ALERTS" >&2
  exit 2
fi
if [[ -n "$ENDPOINT_RESULTS" && ! -f "$ENDPOINT_RESULTS" ]]; then
  echo "Endpoint results not found: $ENDPOINT_RESULTS" >&2
  exit 2
fi

mkdir -p "$OUT_DIR"

PYTHON_BIN="${PYTHON_BIN:-}"
if [[ -z "$PYTHON_BIN" ]]; then
  if command -v python3 >/dev/null 2>&1; then
    PYTHON_BIN="python3"
  elif command -v python >/dev/null 2>&1; then
    PYTHON_BIN="python"
  else
    echo "Neither python3 nor python is available." >&2
    exit 2
  fi
fi

"$PYTHON_BIN" - "$RUN_ID" "$MANIFEST" "$ENDPOINT_RESULTS" "$ALERTS" "$OUT_DIR" <<'PY'
import csv
import json
import os
import sys
from collections import defaultdict

run_id, manifest_path, endpoint_results_path, alerts_path, out_dir = sys.argv[1:6]

with open(manifest_path, "r", encoding="utf-8-sig") as handle:
    manifest = json.load(handle)

rules = {str(rule["id"]): rule for rule in manifest["rules"]}
endpoint = {}
if endpoint_results_path:
    with open(endpoint_results_path, "r", encoding="utf-8-sig") as handle:
        for line in handle:
            line = line.strip()
            if not line:
                continue
            try:
                item = json.loads(line)
            except json.JSONDecodeError:
                continue
            if item.get("run_id") == run_id:
                endpoint[str(item.get("rule_id"))] = item

matches = defaultdict(list)
with open(alerts_path, "r", encoding="utf-8", errors="replace") as handle:
    for line in handle:
        if run_id not in line:
            continue
        try:
            alert = json.loads(line)
        except json.JSONDecodeError:
            continue
        rid = str((alert.get("rule") or {}).get("id") or "")
        if rid in rules:
            matches[rid].append({
                "timestamp": alert.get("timestamp"),
                "agent": (alert.get("agent") or {}).get("name"),
                "level": (alert.get("rule") or {}).get("level"),
                "description": (alert.get("rule") or {}).get("description"),
                "location": alert.get("location"),
            })

summary = []
missing_endpoint_capable = 0
for rid in sorted(rules, key=lambda value: int(value)):
    rule = rules[rid]
    endpoint_item = endpoint.get(rid, {})
    method = rule.get("endpoint_method")
    endpoint_status = endpoint_item.get("status", "not_run")
    hit_count = len(matches.get(rid, []))

    if hit_count > 0:
        verdict = "PASS"
    elif method in ("windows_sysmon_commandline", "windows_powershell_scriptblock", "correlation") and endpoint_status not in ("skipped", "not_run"):
        verdict = "MISSING_ALERT"
        missing_endpoint_capable += 1
    else:
        verdict = "SERVER_SOURCE_REQUIRED"

    summary.append({
        "rule_id": rid,
        "verdict": verdict,
        "endpoint_method": method,
        "endpoint_status": endpoint_status,
        "alert_hits": hit_count,
        "description": rule.get("description"),
        "notes": rule.get("notes"),
        "matches": matches.get(rid, []),
    })

json_path = os.path.join(out_dir, f"local-rule-validation-{run_id}.json")
csv_path = os.path.join(out_dir, f"local-rule-validation-{run_id}.csv")

with open(json_path, "w", encoding="utf-8") as handle:
    json.dump({
        "run_id": run_id,
        "alerts_path": alerts_path,
        "manifest_path": manifest_path,
        "endpoint_results_path": endpoint_results_path,
        "summary": summary,
    }, handle, ensure_ascii=False, indent=2)

with open(csv_path, "w", encoding="utf-8", newline="") as handle:
    writer = csv.DictWriter(handle, fieldnames=["rule_id", "verdict", "endpoint_method", "endpoint_status", "alert_hits", "description"])
    writer.writeheader()
    for item in summary:
        writer.writerow({key: item[key] for key in writer.fieldnames})

passes = sum(1 for item in summary if item["verdict"] == "PASS")
missing = sum(1 for item in summary if item["verdict"] == "MISSING_ALERT")
server_required = sum(1 for item in summary if item["verdict"] == "SERVER_SOURCE_REQUIRED")

print(f"Run ID: {run_id}")
print(f"PASS: {passes}")
print(f"MISSING_ALERT: {missing}")
print(f"SERVER_SOURCE_REQUIRED: {server_required}")
print(f"JSON: {json_path}")
print(f"CSV: {csv_path}")

sys.exit(1 if missing_endpoint_capable else 0)
PY
